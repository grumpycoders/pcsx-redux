    .section .start, "ax", @progbits
    .align 2
    .global main
    .global _start
    .type _start, @function

_start:
    la    $t0, __bss_start
    la    $t1, __bss_end

    beq   $t0, $t1, bss_init_skip

bss_init:
    sw    $0, 0($t0)
    addiu $t0, 4
    bne   $t0, $t1, bss_init

bss_init_skip:

    li    $a0, 0
    li    $a1, 0
    jal   main

    jr    $ra
